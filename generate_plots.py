# PLOT 4: Quantization Error
# ══════════════════════════════════════════════
def plot_error():
    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(14, 6))

    # Left: small demo error (from pytorch_error.csv + cuda_error.csv)
    methods, errors, colors = [], [], []
    if pt_error is not None:
        for _, row in pt_error.iterrows():
            methods.append(row["method"])
            errors.append(row["mean_abs_error"])
            colors.append("#FF9800" if "torchao" in row["method"] else "#4CAF50")
    if cuda_error is not None:
        for _, row in cuda_error.iterrows():
            methods.append(row["method"])
            errors.append(row["mean_abs_error"])
            colors.append("#E91E63")

    ax1.bar(range(len(methods)), errors, color=colors, edgecolor="white")
    for i, (m, e) in enumerate(zip(methods, errors)):
        label = f"{e:.6f}" if e > 0 else "0 (exact)"
        ax1.text(i, e + max(errors) * 0.02, label, ha="center", fontsize=8, fontweight="bold")
    ax1.set_title("Quantization Error (Small Demo)\n(from results/pytorch_error.csv, cuda_error.csv)")
    ax1.set_ylabel("Mean Absolute Error")
    ax1.set_xticks(range(len(methods)))
    ax1.set_xticklabels(methods, fontsize=9, rotation=15)

    # Right: LLM-scale error (from onnx_error.csv)
    if onnx_error is not None:
        int8_err = onnx_error[onnx_error["runtime"] == "ONNX INT8 CPU"]
        configs_e = int8_err["config"].tolist()
        x = np.arange(len(configs_e))
        ax2.bar(x, int8_err["mean_abs_error"].astype(float).values, color="#F44336", edgecolor="white")
        for i, v in enumerate(int8_err["mean_abs_error"].astype(float).values):
            ax2.text(i, v + 0.03, f"{v:.3f}", ha="center", fontsize=9, fontweight="bold")
        ax2.set_title("ONNX INT8 Error at LLM Scale\n(from results/onnx_error.csv)")
        ax2.set_ylabel("Mean Absolute Error vs FP32")
        ax2.set_xticks(x)
        ax2.set_xticklabels(configs_e, fontsize=9)

    plt.tight_layout()
    plt.savefig(os.path.join(PLOT_DIR, "04_quantization_error.png"), bbox_inches="tight")
    plt.close()
    print("  [4/6] Quantization error")


# ══════════════════════════════════════════════
# PLOT 5: Model Size Compression
# ══════════════════════════════════════════════
def plot_model_size():
    if onnx_error is None:
        print("  [5/6] SKIPPED (no onnx_error.csv)")
        return

    int8_rows = onnx_error[(onnx_error["runtime"] == "ONNX INT8 CPU") &
                            (onnx_error["fp32_size_kb"].notna())]

    if len(int8_rows) == 0:
        print("  [5/6] SKIPPED (no size data)")
        return

    fig, ax = plt.subplots(figsize=(10, 6))
    configs_s = int8_rows["config"].tolist()
    fp32_mb = int8_rows["fp32_size_kb"].astype(float).values / 1024
    int8_mb = int8_rows["int8_size_kb"].astype(float).values / 1024

    x = np.arange(len(configs_s))
    width = 0.3
    ax.bar(x - width / 2, fp32_mb, width, label="FP32 (4 bytes/weight)", color="#4CAF50")
    ax.bar(x + width / 2, int8_mb, width, label="INT8 (1 byte/weight)", color="#E91E63")

    for i in range(len(configs_s)):
        ratio = fp32_mb[i] / int8_mb[i]
        ax.text(x[i], fp32_mb[i] * 0.6, f"{ratio:.2f}x\nsmaller",
                ha="center", fontsize=9, fontweight="bold", color="red")

    ax.set_title("ONNX Model Size: FP32 vs INT8\n(from results/onnx_error.csv)")
    ax.set_ylabel("Model Size (MB)")
    ax.set_xticks(x)
    ax.set_xticklabels(configs_s)
    ax.legend()
    plt.tight_layout()
    plt.savefig(os.path.join(PLOT_DIR, "05_model_size.png"), bbox_inches="tight")
    plt.close()
    print("  [5/6] Model size")


