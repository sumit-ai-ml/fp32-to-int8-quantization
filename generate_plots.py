# PLOT 6: Data Path Diagram (static, explanatory)
# ══════════════════════════════════════════════
def plot_data_paths():
    fig, axes = plt.subplots(3, 1, figsize=(14, 10))
    for ax in axes:
        ax.set_xlim(0, 10); ax.set_ylim(0, 2); ax.axis("off")

    def box(ax, x, y, text, color):
        rect = mpatches.FancyBboxPatch((x, y - 0.35), 1.3, 0.7, boxstyle="round,pad=0.1",
                                         facecolor=color, alpha=0.15, edgecolor=color, linewidth=2)
        ax.add_patch(rect)
        ax.text(x + 0.65, y, text, ha="center", va="center", fontsize=9, fontweight="bold", color=color)

    def arrow(ax, x1, y1, x2, y2):
        ax.annotate("", xy=(x2, y2), xytext=(x1, y1), arrowprops=dict(arrowstyle="->", color="#333", lw=2))

    # Path A: CUDA C INT8
    ax = axes[0]
    ax.set_title("CUDA C INT8: True INT8 x INT8 on Tensor Cores (FASTEST)", fontsize=12,
                 color="#E91E63", fontweight="bold", loc="left")
    box(ax, 0.5, 1.0, "INT8\nWeights", "#E91E63")
    arrow(ax, 2.0, 1.0, 2.8, 1.0)
    box(ax, 3.0, 1.0, "Tensor Core\nIMMA\nINT8xINT8", "#E91E63")
    arrow(ax, 4.5, 1.0, 5.3, 1.0)
    box(ax, 5.5, 1.0, "INT32\nAccum", "#9C27B0")
    arrow(ax, 7.0, 1.0, 7.8, 1.0)
    box(ax, 8.0, 1.0, "FP32\nOutput", "#4CAF50")
    ax.text(5, 0.2, "No dequant before matmul -- 4x less data + native INT8 compute",
            ha="center", fontsize=10, style="italic", color="#E91E63")

    # Path B: PyTorch torchao
    ax = axes[1]
    ax.set_title("PyTorch torchao INT8: Dequant to FP32 first, then FP32 GEMM (SLOWEST)", fontsize=12,
                 color="#FF9800", fontweight="bold", loc="left")
    box(ax, 0.5, 1.0, "INT8\nWeights", "#FF9800")
    arrow(ax, 2.0, 1.0, 2.8, 1.0)
    box(ax, 3.0, 1.0, "DEQUANT\nINT8->FP32\n55% time!", "#F44336")
    arrow(ax, 4.5, 1.0, 5.3, 1.0)
    box(ax, 5.5, 1.0, "FP32\nGEMM", "#2196F3")
    arrow(ax, 7.0, 1.0, 7.8, 1.0)
    box(ax, 8.0, 1.0, "FP32\nOutput", "#4CAF50")
    ax.text(5, 0.2, "Dequant overhead (55% of GPU time) makes it SLOWER than plain FP32",
            ha="center", fontsize=10, style="italic", color="#F44336")

    # Path C: ONNX INT8 CPU
    ax = axes[2]
    ax.set_title("ONNX INT8 CPU: Native INT8 GEMM via MLAS (fast for small matrices)", fontsize=12,
                 color="#F44336", fontweight="bold", loc="left")
    box(ax, 0.5, 1.0, "INT8\nWeights", "#F44336")
    arrow(ax, 2.0, 1.0, 2.8, 1.0)
    box(ax, 3.0, 1.0, "Quantize\nActivations\n(runtime)", "#FF5722")
    arrow(ax, 4.5, 1.0, 5.3, 1.0)
    box(ax, 5.5, 1.0, "MLAS\nINT8xINT8\n(VNNI)", "#F44336")
    arrow(ax, 7.0, 1.0, 7.8, 1.0)
    box(ax, 8.0, 1.0, "FP32\nOutput", "#4CAF50")
    ax.text(5, 0.2, "No GPU launch overhead -- small weights fit in CPU L3 cache",
            ha="center", fontsize=10, style="italic", color="#F44336")

    plt.tight_layout()
    plt.savefig(os.path.join(PLOT_DIR, "06_data_paths.png"), bbox_inches="tight")
    plt.close()
    print("  [6/6] Data path diagrams")


