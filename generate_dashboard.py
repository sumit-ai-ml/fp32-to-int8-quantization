def main():
    print("Loading CSVs from results/ ...")
    pt_timing = load_csv("pytorch_timing.csv")
    cuda_timing = load_csv("cuda_timing.csv")
    onnx_timing = load_csv("onnx_timing.csv")
    profiling = load_csv("profiling.csv")
    pt_error = load_csv("pytorch_error.csv")
    onnx_error = load_csv("onnx_error.csv")
    cuda_error = load_csv("cuda_error.csv")
    ollama_timing = load_csv("ollama_timing.csv")

    # Merge timing data
    all_timing = pd.concat([
        df for df in [pt_timing, cuda_timing, onnx_timing] if df is not None
    ], ignore_index=True)
    all_timing["time_ms"] = pd.to_numeric(all_timing["time_ms"], errors="coerce")
    if "std_ms" in all_timing.columns:
        all_timing["std_ms"] = pd.to_numeric(all_timing["std_ms"], errors="coerce").fillna(0)
    timing = all_timing[all_timing["config"].isin(COMMON_CONFIGS)].copy()

    print("Building charts...")
    fig1 = build_inference_chart(timing)
    print("  [1/5] Inference time")

    fig2 = build_speedup_heatmap(timing)
    print("  [2/5] Speedup heatmap")

    fig3 = build_core_breakdown(profiling)
    print("  [3/5] Core breakdown")

    fig4 = build_error_chart(pt_error, onnx_error, cuda_error)
    print("  [4/5] Quantization error")

    fig5 = build_model_size(onnx_error)
    print("  [5/5] Model size")

    ollama_figs = build_ollama_section(ollama_timing)
    if ollama_figs[0] is not None:
        print("  [+] Ollama section")

    print("Assembling HTML...")
    html = build_html([fig1, fig2, fig3, fig4, fig5], ollama_figs)

    with open(OUTPUT_FILE, "w") as f:
        f.write(html)

    size_mb = os.path.getsize(OUTPUT_FILE) / (1024 * 1024)
    print(f"\nDashboard written to: {OUTPUT_FILE} ({size_mb:.1f} MB)")


if __name__ == "__main__":
    main()
